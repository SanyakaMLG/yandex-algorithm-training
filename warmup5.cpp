#include <iostream>

int main() {
    int n;
    std::cin >> n;
    long long ans = 0;
    int cur;
    std::cin >> cur;
    int x;
    for (int i = 0; i < n - 1; i++) {
        std::cin >> x;
        ans += std::min(cur, x);
        cur = x;
    }
    std::cout << ans;
    return 0;
}

