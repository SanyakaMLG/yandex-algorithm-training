#include <iostream>

int main() {
    std::string str;
    getline(std::cin, str);
    long long array[26] = {0};
    long long len = str.length();
    for (int i = 0; i < len; i++) {
        array[str[i] - 'a'] += (i + 1) * (len - i);
    }
    for (int i = 0; i < 26; i++) {
        if (array[i]) {
            std::cout << char(i + 'a') << ": " << array[i] << std::endl;
        }
    }
}

